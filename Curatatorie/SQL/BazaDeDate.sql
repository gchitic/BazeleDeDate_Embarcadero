CREATE TABLE SERVICII
(
	SERVICII_ID INT PRIMARY KEY IDENTITY(1,1),
	DENUMIRE VARCHAR(100)
)

CREATE TABLE CLIENT
(
	CLIENT_ID INT PRIMARY KEY IDENTITY(1,1),
	NUME_CLIENT VARCHAR(100),
	NR_TEL VARCHAR(50)
)

CREATE TABLE OPERATOR
(
	OPERATOR_ID INT PRIMARY KEY IDENTITY(1,1),
	NUME VARCHAR(50),
	PRENUME VARCHAR(50),
	USERNAME VARCHAR(10),
	PAROLA VARCHAR(10)
)

CREATE TABLE COMANDA
(
	COMANDA_ID INT PRIMARY KEY IDENTITY(1,1),
	DENUMIRE VARCHAR(200),
	SERVICII_ID INT FOREIGN KEY REFERENCES SERVICII(SERVICII_ID),
	DATA_INREG DATE,
	DATA_INTOARC DATE,
	CLIENT_ID INT FOREIGN KEY REFERENCES CLIENT(CLIENT_ID),
	OPERATOR_ID INT FOREIGN KEY REFERENCES OPERATOR(OPERATOR_ID),
	PRET DECIMAL(9,2)
)

CREATE TABLE CEC
(
	CEC_ID INT PRIMARY KEY IDENTITY(1,1),
	COMANDA_ID INT FOREIGN KEY REFERENCES COMANDA(COMANDA_ID),
	DATA_CEC DATE,
	SUMA_CEC DECIMAL(9,2)
)

